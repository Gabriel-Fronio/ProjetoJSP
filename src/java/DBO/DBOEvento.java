package DBO;

import java.sql.Date;

/**
 *
 * @author u16166
 */
public class DBOEvento {

    public DBOEvento(String nome, Date data) {
        this.nome = nome;
        this.data = data;
    }

    /**
     * @return the nome
     */
    public String getNome() {
        return nome;
    }

    /**
     * @param nome the nome to set
     */
    public void setNome(String nome) {
        this.nome = nome;
    }

    /**
     * @return the data
     */
    public Date getData() {
        return data;
    }

    /**
     * @param data the data to set
     */
    public void setData(Date data) {
        this.data = data;
    }
    private String nome;
    private Date data;
}
